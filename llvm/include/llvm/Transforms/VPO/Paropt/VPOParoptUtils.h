  /// This can be used to initialize min/max reduction variables.
  static Constant *getMinMaxIntVal(LLVMContext &C, Type *Ty, bool IsUnsigned,
                                   bool GetMax);
  // static uint64_t getMinInt(Type *IntTy, bool IsUnsigned);
