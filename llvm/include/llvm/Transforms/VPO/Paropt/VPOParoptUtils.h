  static bool genKmpcCriticalSectionImpl(WRegionNode *W, StructType *IdentTy,
                                         Constant *TidPtr,
                                         Instruction *BeginInst,
                                         Instruction *EndInst,
                                         GlobalVariable *LockVar,
                                         bool IsTargetSPIRV);

