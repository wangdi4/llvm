#include <stdio.h>
#include <string.h>

int main(int /*argc*/, char **argv) {
  FILE *input;
  FILE *output;

  if (!argv[1] || !strcmp(argv[1], "-h") || !strcmp(argv[1], "--help") ||
      !argv[2] || !argv[3]) {
    printf("Usage: %s [input file] [output file] [symbol base name]\n",
           argv[0]);
    return 0;
  }

  input = fopen(argv[1], "rb");
  if (!input) {
    printf("Unable to open input file for reading (%s)\n", argv[1]);
    return 1;
  }

  output = fopen(argv[2], "wb");
  if (!output) {
    printf("Unable to open output file for writing (%s)\n", argv[2]);
    return 1;
  }

  fprintf(output, "// This file is auto generated by %s, DO NOT EDIT\n\n",
          argv[0]);
  fprintf(output, "extern const char %s[] =\n{\n    ", argv[3]);

  int count = 0;
  int is_eol;
  unsigned char data;
  while (fread(&data, 1, 1, input)) {
    is_eol = count ? !(count % 20) : 0;
    fprintf(output, "%s0x%.2x, ", is_eol ? "\n    " : "", data);
    count++;
  }
  fprintf(output, "0x00\n};\n\n");

  fprintf(output, "unsigned int %s_size = %d;\n\n", argv[3], count);

  fprintf(output, "extern const char %s_name[] = \"%s\";\n\n", argv[3],
          argv[3]);

  fclose(input);
  fclose(output);
  return 0;
}
