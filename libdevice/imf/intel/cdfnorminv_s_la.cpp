inline int __devicelib_imf_internal_scdfnorminv(const float *a, float *r) 
{
  int nRet = 0;
  // cdfnorminv = sqrt(2)*erfcinv(2*(1-x))
  float xin = 2.0f * (1.0f - (*a));
  float apoly, bpoly, lxin, xin2, xf;
  float lR, lpoly, aR0, aR, bR0, bR;
  int_float x, expon, m, res;
  int iexpon;
  xin2 = 2.0f * (*a);
  lxin = __fma(xin, xin2, 0.0f);
  xf = __fmin(xin, xin2);
  // input to log2()
  lxin = (xf < __scdfnorminv_la___twom9.f) ? xf : lxin;
  // compute log2(lxin),  lxin in (0,1)
  x.f = lxin;
  x.f *= (float)(1 << 26);
  expon.w = ((x.w + 0x00555555) >> 23) - 0x7f;
  m.w = x.w - (expon.w << 23);
  lR = m.f - 1;
  expon.w -= 26;
  iexpon = expon.w;
  lpoly = __fma(lR, __scdfnorminv_la___cl8.f,
                                 __scdfnorminv_la___cl7.f);
  lpoly = __fma(lpoly, lR, __scdfnorminv_la___cl6.f);
  lpoly = __fma(lpoly, lR, __scdfnorminv_la___cl5.f);
  lpoly = __fma(lpoly, lR, __scdfnorminv_la___cl4.f);
  lpoly = __fma(lpoly, lR, __scdfnorminv_la___cl3.f);
  lpoly = __fma(lpoly, lR, __scdfnorminv_la___cl2.f);
  lpoly = __fma(lpoly, lR, __scdfnorminv_la___cl1.f);
  lpoly = __fma(lpoly, lR, __scdfnorminv_la___cl0.f);
  lpoly = __fma(lpoly, lR, ((float)iexpon));
  aR0 = -lpoly;
  aR = 1.0f / __sqrt(aR0);
  apoly = __fma(__scdfnorminv_la___cp09.f, aR,
                                 __scdfnorminv_la___cp08.f);
  apoly = __fma(apoly, aR, __scdfnorminv_la___cp07.f);
  apoly = __fma(apoly, aR, __scdfnorminv_la___cp06.f);
  apoly = __fma(apoly, aR, __scdfnorminv_la___cp05.f);
  apoly = __fma(apoly, aR, __scdfnorminv_la___cp04.f);
  apoly = __fma(apoly, aR, __scdfnorminv_la___cp03.f);
  apoly = __fma(apoly, aR, __scdfnorminv_la___cp02.f);
  apoly = __fma(apoly, aR, __scdfnorminv_la___cp01.f);
  apoly = __fma(apoly, aR, __scdfnorminv_la___cp00.f);
  res.f = 1.0f / (apoly);
  res.f *= __scdfnorminv_la___sqrt2.f;
  res.w = (xf == 0.0f) ? 0x7f800000 : res.w;
  nRet = (xf == 0.0f) ? 2 : nRet;
  res.w = (xf >= 0.0f) ? res.w : 0xffc00000;
  nRet = (xf >= 0.0f) ? nRet : 1;
  // set sign if result
  res.w = (xin < 1.0f) ? res.w : (res.w | 0x80000000);
  *r = res.f;
  return nRet;
}

